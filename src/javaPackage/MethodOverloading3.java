package javaPackage;

public class MethodOverloading3 {

	public static int add (int a, int b) //user defined static method - it needs int return type and return type statement
	{
		return a+b;
	}
	
	public static int add (int a, int b, int c) //user defined static method - it needs int return type and return type statement 
	{
		return a+b+c;
	}

	

}
