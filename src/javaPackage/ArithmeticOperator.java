package javaPackage;

public class ArithmeticOperator {

	public static void main(String[] args) {
		
		int a=20;
		int b = 20;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		
		System.out.println(a+b+b);
		System.out.println(a+b*a);
		System.out.println(a+b/a);
		
	}

}
